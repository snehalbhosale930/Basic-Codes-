
namespace BasicCodes
{
    internal class MathFunction
    {
       public static int Add(int a, int b)
        {
            return a + b;
        }
        public static int sub(int a, int b)
        {
            return a - b;
        }
        public static int Mul(int a,int b, int c)
        {
            return a * b + c;
        }
        public static int Div(int a, int b, int c)
        {
            return a / b + c;
        }
    }
}
 